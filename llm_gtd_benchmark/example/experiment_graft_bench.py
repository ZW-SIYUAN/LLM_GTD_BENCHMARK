def parse_args():
    p = argparse.ArgumentParser(
        description="GraFT benchmark: GReaT vs PAFT on diabetes/house/income/sick",
        formatter_class=argparse.ArgumentDefaultsHelpFormatter,
    )
    p.add_argument("--dataset",       choices=list(DATASET_CONFIGS.keys()), required=True)
    p.add_argument("--mode",          choices=["great", "paft"], required=True,
                   help="great=random order baseline; paft=HyFD-derived fixed order")
    p.add_argument("--output_dir",    default=None,
                   help="Output dir (default: output_graft_<dataset>_<mode>)")
    p.add_argument("--llm",           default="gpt2-medium")
    p.add_argument("--learning_rate", type=float, default=5e-5)
    p.add_argument("--lora_r",        type=int,   default=8)
    p.add_argument("--lora_alpha",    type=int,   default=16)
    p.add_argument("--lora_dropout",  type=float, default=0.05)
    p.add_argument("--bf16",          action="store_true", default=True)
    p.add_argument("--no-bf16",       dest="bf16", action="store_false")
    p.add_argument("--temperature",   type=float, default=0.8)
    return p.parse_args()


if __name__ == "__main__":
    args = parse_args()
    if args.output_dir is None:
        args.output_dir = os.path.join(
            HERE, f"output_graft_{args.dataset}_{args.mode}"
        )
    logger.info(f"Dataset: {args.dataset}  Mode: {args.mode}  "
                f"Output: {args.output_dir}")
    run_experiment(args)
