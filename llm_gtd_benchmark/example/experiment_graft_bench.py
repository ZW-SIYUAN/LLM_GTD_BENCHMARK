def _git_hash():
    try:
        return subprocess.check_output(
            ["git", "rev-parse", "--short", "HEAD"],
            cwd=TIDE_ROOT, stderr=subprocess.DEVNULL,
        ).decode().strip()
    except Exception:
        return "unknown"


def _read_final_loss(trainer_dir):
    if not os.path.isdir(trainer_dir):
        return None
    ckpts = sorted(
        [d for d in os.listdir(trainer_dir) if d.startswith("checkpoint-")],
        key=lambda x: int(x.split("-")[-1]),
    )
    candidates = []
    if ckpts:
        candidates.append(os.path.join(trainer_dir, ckpts[-1], "trainer_state.json"))
    candidates.append(os.path.join(trainer_dir, "trainer_state.json"))
    for path in candidates:
        if os.path.exists(path):
            with open(path, encoding="utf-8") as f:
                history = json.load(f).get("log_history", [])
            losses = [e["loss"] for e in history if "loss" in e]
            if losses:
                return round(float(losses[-1]), 6)
    return None


