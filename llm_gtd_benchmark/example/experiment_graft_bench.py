def run_experiment(args):
    t0  = time.time()
    cfg = DATASET_CONFIGS[args.dataset]

    os.makedirs(args.output_dir, exist_ok=True)

    # ── Load data ─────────────────────────────────────────────────────────────
    df_train = pd.read_csv(cfg["train_csv"])
    df_test  = pd.read_csv(cfg["test_csv"])
    # Drop columns that are entirely NaN (e.g. TBG in sick), then drop fully-empty rows
    df_train = df_train.dropna(axis=1, how="all").dropna(how="all")
    df_test  = df_test.dropna(axis=1, how="all").dropna(how="all")
    # Sync columns: keep only columns present in both splits
    shared_cols = [c for c in df_train.columns if c in df_test.columns]
    df_train = df_train[shared_cols]
    df_test  = df_test[shared_cols]
    logger.info(f"Dataset: {args.dataset}  train={len(df_train)}  test={len(df_test)}")

    n_samples = len(df_train)

    # ── Load HyFD-derived PAFT order ─────────────────────────────────────────
    paft_order = HYFD_PAFT_ORDERS[args.dataset]
    paft_order = paft_order + [c for c in df_train.columns if c not in paft_order]
    logger.info(f"PAFT order (HyFD): {paft_order}")

    # ── Build model ───────────────────────────────────────────────────────────
    trainer_dir = os.path.join(args.output_dir, "trainer")
    lora_cfg = dict(r=args.lora_r, lora_alpha=args.lora_alpha,
                    lora_dropout=args.lora_dropout)

    if args.mode == "paft":
        sys.path.insert(0, TIDE_ROOT)
        from ib_order_finder import FixedOrderGReaT

        model = FixedOrderGReaT(
            fixed_order=paft_order,
            llm=args.llm,
            experiment_dir=trainer_dir,
            epochs=cfg["epochs"],
            batch_size=cfg["batch_size"],
            efficient_finetuning="lora",
            lora_config=lora_cfg,
        )
        sample_k = cfg["sample_k_fixed"]
        mode_tag = "paft"
    else:
        from be_great import GReaT
        model = GReaT(
            llm=args.llm,
            experiment_dir=trainer_dir,
            epochs=cfg["epochs"],
            batch_size=cfg["batch_size"],
            efficient_finetuning="lora",
            lora_config=lora_cfg,
        )
        sample_k = cfg["sample_k_random"]
        mode_tag = "great"

    # Apply global hyperparameters
    model.train_hyperparameters["learning_rate"] = args.learning_rate
    if args.bf16:
        model.train_hyperparameters["bf16"] = True

    # ── Train ─────────────────────────────────────────────────────────────────
    logger.info(f"Training ({mode_tag})  epochs={cfg['epochs']}  "
                f"batch={cfg['batch_size']}  lr={args.learning_rate}  lora_r={args.lora_r}")
    model.fit(df_train)

    model_save = os.path.join(args.output_dir, "model")
    model.save(model_save)
    logger.info(f"Model saved: {model_save}")

    # ── Sample ────────────────────────────────────────────────────────────────
    logger.info(f"Sampling  n={n_samples}  T={args.temperature}  "
                f"k={sample_k}  max_length={cfg['max_length']}")
    syn_df = model.sample(
        n_samples=n_samples,
        guided_sampling=False,
        temperature=args.temperature,
        k=sample_k,
        max_length=cfg["max_length"],
    )
    logger.info(f"Sampled {len(syn_df)} rows")

    syn_csv = os.path.join(args.output_dir, f"synthetic_{args.dataset}.csv")
    syn_df.to_csv(syn_csv, index=False, encoding="utf-8")
    logger.info(f"Synthetic data saved: {syn_csv}")

    # ── Metrics ───────────────────────────────────────────────────────────────
    logger.info("Computing metrics...")

    tstr_metrics = compute_tstr(syn_df, df_train, df_test, cfg)
    col_metrics  = compute_column_stats(syn_df, df_train, cfg)
    ratio_copies = compute_ratio_copies(syn_df, df_train)
    logger.info(f"  ratio_copies = {ratio_copies:.4f}")
    mle_f1 = compute_mle(df_train, df_test, cfg)

    metrics_all = {
        **tstr_metrics,
        **col_metrics,
        "ratio_copies": ratio_copies,
        "mle_f1_macro": mle_f1,
    }

    metrics_path = os.path.join(args.output_dir, "metrics.json")
    with open(metrics_path, "w", encoding="utf-8") as f:
        json.dump(metrics_all, f, ensure_ascii=False, indent=2)
    logger.info(f"Metrics saved: {metrics_path}")

    # ── Log ───────────────────────────────────────────────────────────────────
    elapsed = time.time() - t0
    record = {
        "timestamp":        datetime.datetime.now().strftime("%Y-%m-%d %H:%M:%S"),
        "experiment":       f"graft_bench/{args.dataset}/{mode_tag}",
        "dataset":          args.dataset,
        "mode":             mode_tag,
        "git_hash":         _git_hash(),
        "output_dir":       args.output_dir,
        # model
        "llm":              args.llm,
        "learning_rate":    args.learning_rate,
        "lora_r":           args.lora_r,
        "lora_alpha":       args.lora_alpha,
        "lora_dropout":     args.lora_dropout,
        "bf16":             args.bf16,
        "temperature":      args.temperature,
        # train
        "epochs":           cfg["epochs"],
        "batch_size":       cfg["batch_size"],
        "max_length":       cfg["max_length"],
        "sample_k":         sample_k,
        "n_train":          len(df_train),
        "n_synthetic":      len(syn_df),
        "paft_order":       paft_order if mode_tag == "paft" else None,
        # timing
        "elapsed_seconds":  round(elapsed, 1),
        "final_loss":       _read_final_loss(trainer_dir),
        # metrics
        **metrics_all,
    }
    with open(LOG_FILE, "a", encoding="utf-8") as f:
        f.write(json.dumps(record, ensure_ascii=False) + "\n")
    logger.info(f"Run logged: {LOG_FILE}")

    logger.info("=" * 60)
    tstr_val = tstr_metrics.get("tstr_f1_macro")
    tstr_str = f"{tstr_val:.4f}" if tstr_val is not None else "N/A"
    logger.info(f"Done  {args.dataset}/{mode_tag}  "
                f"TSTR={tstr_str}  MLE={mle_f1:.4f}  "
                f"ratio_copies={ratio_copies:.4f}  "
                f"elapsed={elapsed/60:.1f}min")
    logger.info("=" * 60)


