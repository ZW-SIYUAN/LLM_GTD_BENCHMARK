"""
GraFT Benchmark Experiment Runner
==================================
Runs GReaT (random order) and PAFT (HyFD-derived fixed order) on four benchmark
datasets: diabetes, house, income, sick.

Usage:
    python experiment_graft_bench.py --dataset diabetes --mode great
    python experiment_graft_bench.py --dataset diabetes --mode paft
    python experiment_graft_bench.py --dataset house    --mode paft
    python experiment_graft_bench.py --dataset income   --mode great
    python experiment_graft_bench.py --dataset sick     --mode paft

Global defaults (both modes):
    LLM: gpt2-medium, lr=5e-5, LoRA r=8/alpha=16/dropout=0.05, bf16,
    temperature=0.8, top_p=1.0 (default, not exposed by be_great sample())
    PAFT order: derived by HyFD (run_hyfd_and_get_order.py)

Note on unsupported params:
    top_p=1.0       -- be_great.sample() does not expose top_p; default HF
                       generate uses no nucleus filtering (equivalent to top_p=1.0)
    sparsity_lambda / fd_lambda / fd_alpha / num_head_groups
                    -- TIDE IB-specific params, not applicable to vanilla GReaT/PAFT
"""

