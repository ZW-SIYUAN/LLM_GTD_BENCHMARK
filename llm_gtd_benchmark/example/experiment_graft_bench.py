def compute_tstr(syn_df, real_train_df, real_test_df, cfg):
    """Train on Synthetic, Test on Real — macro-F1 for the target column."""
    from sklearn.ensemble import RandomForestClassifier
    from sklearn.metrics import f1_score
    from sklearn.preprocessing import LabelEncoder

    target = cfg["target_col"]
    feat_cols = [c for c in cfg["num_cols"] if c != target]

    def _extract(df, cols, target_col, le=None):
        X_raw = df[cols].copy()
        for c in X_raw.columns:
            X_raw[c] = pd.to_numeric(X_raw[c], errors="coerce")
        X_raw = X_raw.fillna(X_raw.median())
        y_raw = df[target_col].astype(str).values
        if le is None:
            le = LabelEncoder()
            y_enc = le.fit_transform(y_raw)
        else:
            valid_mask = np.isin(y_raw, le.classes_)
            X_raw = X_raw[valid_mask]
            y_enc = le.transform(y_raw[valid_mask])
        return X_raw.values, y_enc, le

    syn_clean = syn_df.dropna(subset=[target]).copy() if target in syn_df.columns else None
    if syn_clean is None or len(syn_clean) == 0:
        logger.warning("TSTR skipped: target column missing or empty in synthetic data")
        return {"tstr_f1_macro": None, "tstr_n_syn_train": 0}

    X_syn, y_syn, le = _extract(syn_clean, feat_cols, target)
    X_test, y_test, _ = _extract(real_test_df, feat_cols, target, le=le)

    if len(np.unique(y_syn)) < 2:
        logger.warning("TSTR skipped: fewer than 2 classes in synthetic target")
        return {"tstr_f1_macro": None, "tstr_n_syn_train": int(len(X_syn))}

    clf = RandomForestClassifier(n_estimators=100, random_state=42, n_jobs=-1)
    clf.fit(X_syn, y_syn)
    y_pred = clf.predict(X_test)

    f1 = float(f1_score(y_test, y_pred, average="macro", zero_division=0))
    logger.info(f"  TSTR macro-F1 = {f1:.4f}  (train={len(X_syn)}, test={len(X_test)})")
    return {"tstr_f1_macro": f1, "tstr_n_syn_train": int(len(X_syn))}


def compute_mle(real_train_df, real_test_df, cfg):
    """MLE (real-train -> real-test) as upper bound reference."""
    from sklearn.ensemble import RandomForestClassifier
    from sklearn.metrics import f1_score
    from sklearn.preprocessing import LabelEncoder

    target    = cfg["target_col"]
    feat_cols = [c for c in cfg["num_cols"] if c != target]

    le = LabelEncoder().fit(real_train_df[target].astype(str).values)
    X_tr = real_train_df[feat_cols].apply(pd.to_numeric, errors="coerce").fillna(0).values
    y_tr = le.transform(real_train_df[target].astype(str).values)
    X_te = real_test_df[feat_cols].apply(pd.to_numeric, errors="coerce").fillna(0).values
    mask = np.isin(real_test_df[target].astype(str).values, le.classes_)
    X_te = X_te[mask]
    y_te = le.transform(real_test_df[target].astype(str).values[mask])

    clf = RandomForestClassifier(n_estimators=100, random_state=42, n_jobs=-1)
    clf.fit(X_tr, y_tr)
    y_pred = clf.predict(X_te)
    f1 = float(f1_score(y_te, y_pred, average="macro", zero_division=0))
    logger.info(f"  MLE  macro-F1 = {f1:.4f}  (upper bound)")
    return f1


def compute_column_stats(syn_df, real_df, cfg):
    """Per-column coverage (cat) and mean/std relative error (num)."""
    metrics = {}
    for col in cfg["cat_cols"]:
        if col not in syn_df.columns:
            continue
        real_vals = set(real_df[col].dropna().astype(str).unique())
        syn_vals  = syn_df[col].dropna().astype(str)
        cov = float(syn_vals.isin(real_vals).mean()) if len(syn_vals) > 0 else 0.0
        metrics[f"{col}_coverage"] = cov

    for col in cfg["num_cols"]:
        if col not in syn_df.columns:
            continue
        real_s = pd.to_numeric(real_df[col], errors="coerce").dropna()
        syn_s  = pd.to_numeric(syn_df[col],  errors="coerce").dropna()
        if len(syn_s) == 0:
            continue
        rmean, smean = real_s.mean(), syn_s.mean()
        rstd,  sstd  = real_s.std(),  syn_s.std()
        metrics[f"{col}_mean_err"] = float(abs(smean - rmean) / (abs(rmean) + 1e-8))
        metrics[f"{col}_std_err"]  = float(abs(sstd  - rstd)  / (abs(rstd)  + 1e-8))
    return metrics


def compute_ratio_copies(syn_df, real_df):
    """Fraction of synthetic rows that exactly match a real training row."""
    cols = [c for c in syn_df.columns if c in real_df.columns]
    if not cols:
        return 0.0
    merged = syn_df[cols].merge(real_df[cols].drop_duplicates(), on=cols, how="inner")
    return float(len(merged) / max(len(syn_df), 1))


